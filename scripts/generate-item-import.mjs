// scripts/generate-item-import.mjs
// Reads ItemMaster.csv and generates supabase/migrations/019_item_master_import.sql

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))

function parseCSVLine(line) {
  const result = []
  let inQuotes = false
  let current = ''
  for (let i = 0; i < line.length; i++) {
    const char = line[i]
    if (char === '"') {
      if (inQuotes && line[i + 1] === '"') { current += '"'; i++ }
      else inQuotes = !inQuotes
    } else if (char === ',' && !inQuotes) {
      result.push(current); current = ''
    } else {
      current += char
    }
  }
  result.push(current)
  return result
}

function sqlStr(s) {
  if (s === null || s === undefined || String(s).trim() === '') return 'NULL'
  return `'${String(s).trim().replace(/'/g, "''")}'`
}

const csvPath = path.join(__dirname, 'ItemMaster.csv')
const csvContent = fs.readFileSync(csvPath, 'utf-8')
const lines = csvContent.split(/\r?\n/)

const items = []
for (let i = 1; i < lines.length; i++) {
  const line = lines[i]
  if (!line.trim()) continue
  const cols = parseCSVLine(line)
  const groupCode = cols[0]?.trim()
  const itemCode  = cols[1]?.trim()
  const itemName  = cols[2]?.trim()
  const size      = cols[3]?.trim()
  if (!groupCode || !itemCode || !itemName) continue
  items.push({ groupCode, itemCode, itemName, size: size || null })
}

console.log(`Parsed ${items.length} items`)

const materialTypeMap = {
  CR:  'CR',
  GA:  'GA',
  GI:  'GI',
  HR:  'HRPO',
  OTH: 'OTH',
}

const groupDescMap = {
  CR:  'Cold Rolled Steel Coils and Sheets',
  GA:  'Galvannealed Steel Coils and Products',
  GI:  'Galvanized Iron Coils and Sheets',
  HR:  'Hot Rolled Steel Coils and Channels',
  OTH: 'Other Materials and Miscellaneous Items',
}

const groups = [...new Set(items.map(i => i.groupCode))].filter(Boolean).sort()
console.log('Groups:', groups.join(', '))

const sizesByGroup = {}
for (const item of items) {
  if (!item.groupCode || !item.size) continue
  if (!sizesByGroup[item.groupCode]) sizesByGroup[item.groupCode] = new Set()
  sizesByGroup[item.groupCode].add(item.size)
}
for (const [g, s] of Object.entries(sizesByGroup)) console.log(`  ${g}: ${s.size} unique sizes`)

const parts = []

parts.push(`-- ============================================================
-- Migration: 019_item_master_import.sql
-- Generated by: scripts/generate-item-import.mjs
-- Source:       scripts/ItemMaster.csv
-- ============================================================
-- Steps:
--   0. Ensure OTH material type exists
--   1. Clear item_master, material_sizes (per group type), item_groups
--   2. Insert 5 Item Groups (descriptions are placeholders)
--   3. Insert unique Material Sizes per material type
--   4. Upsert all Item Master records
-- ============================================================

BEGIN;

-- Step 0: Ensure OTH material type exists (other types CR/GA/GI/HRPO already seeded)
INSERT INTO material_types (name, unit, description)
VALUES ('OTH', 'tons', 'Other Materials and Miscellaneous Items')
ON CONFLICT (name) DO NOTHING;

-- Step 1: Clear master data (order matters — item_master references both)
DELETE FROM item_master;
DELETE FROM material_sizes WHERE material_type_id IN (
  SELECT id FROM material_types
  WHERE name IN ('CR','GA','GI','HRPO','OTH')
);
DELETE FROM item_groups;

-- Step 2: Item Groups (edit group_desc as needed)
INSERT INTO item_groups (group_code, group_desc) VALUES
${groups.map(g => `  (${sqlStr(g)}, ${sqlStr(groupDescMap[g] || g)})`).join(',\n')};`)

parts.push(`-- Step 3: Material Sizes`)
for (const groupCode of groups) {
  const materialType = materialTypeMap[groupCode]
  const sizes = sizesByGroup[groupCode]
  if (!sizes || sizes.size === 0) continue
  const sizeList = [...sizes].sort().map(s => `  (${sqlStr(s)})`).join(',\n')
  parts.push(`-- ${groupCode} → ${materialType} (${sizes.size} sizes)
INSERT INTO material_sizes (material_type_id, size_label)
SELECT mt.id, s.size_label
FROM material_types mt
CROSS JOIN (VALUES
${sizeList}
) AS s(size_label)
WHERE mt.name = ${sqlStr(materialType)}
ON CONFLICT (material_type_id, size_label) DO NOTHING;`)
}

const itemRows = items.map(item =>
  `  (${sqlStr(item.groupCode)}, ${sqlStr(item.itemCode)}, ${sqlStr(item.itemName)}, ${sqlStr(materialTypeMap[item.groupCode])}, ${sqlStr(item.size)})`
)

parts.push(`-- Step 4: Item Master (${items.length} items)
INSERT INTO item_master (item_code, item_name, item_group_id, material_type_id, material_size_id, size_label)
SELECT
  v.item_code,
  v.item_name,
  ig.id,
  mt.id,
  ms.id,
  v.size_label
FROM (VALUES
${itemRows.join(',\n')}
) AS v(group_code, item_code, item_name, material_type_name, size_label)
JOIN item_groups ig ON ig.group_code = v.group_code
JOIN material_types mt ON mt.name = v.material_type_name
LEFT JOIN material_sizes ms
  ON ms.material_type_id = mt.id
  AND ms.size_label = v.size_label
  AND v.size_label IS NOT NULL
ON CONFLICT (item_code) DO UPDATE SET
  item_name        = EXCLUDED.item_name,
  item_group_id    = EXCLUDED.item_group_id,
  material_type_id = EXCLUDED.material_type_id,
  material_size_id = EXCLUDED.material_size_id,
  size_label       = EXCLUDED.size_label,
  updated_at       = NOW();`)

const totalSizes = Object.values(sizesByGroup).reduce((a, s) => a + s.size, 0)
parts.push(`COMMIT;
-- Summary: ${items.length} items | ${totalSizes} unique sizes | ${groups.length} groups`)

const sql = parts.join('\n\n')
const outPath = path.join(__dirname, '..', 'supabase', 'migrations', '019_item_master_import.sql')
fs.writeFileSync(outPath, sql, 'utf-8')
console.log(`\nWritten: ${outPath}`)
console.log(`SQL lines: ${sql.split('\n').length}`)
